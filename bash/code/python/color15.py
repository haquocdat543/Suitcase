def odd_or_even(*args):
  """Function that takes a variable number of positional arguments.

  Args:
      *args: A tuple containing all provided positional arguments.
  """

  colored_string = ""
  Init = 0

  for arg in args:

    # Define initial order
    Init = Init + 1

    if ( Init % 2 == 0 ):
      content = arg
    else:
      heximal = arg

    # Implement print color
    if ( Init % 2 == 0 ):
      colored_string = colored_string + print_text_color(heximal, content)
