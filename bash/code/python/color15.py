def rgb(heximal, background=False):

  # Assign heximal to word
  word = heximal

  # Get the first two characters using slicing
  red = word[0:2]
  green = word[2:4]
  blue = word[4:6]

  # Convert RGB to decimal
  red = hex_to_dec(red)
  green = hex_to_dec(green)
  blue = hex_to_dec(blue)

  # Convert RGB to integer
  red = int(red)
  green = int(green)
  blue = int(blue)

  # Pass RGB to function
  return '\033[{};2;{};{};{}m'.format(48 if background else 38, red, green, blue)

